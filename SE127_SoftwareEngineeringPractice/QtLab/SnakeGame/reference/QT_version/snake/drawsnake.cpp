#include "drawsnake.h"




DrawSnake::DrawSnake()
{
    this->setFixedWidth(STANDARD_WIDTH);
    this->setFixedHeight(STANDARD_HEIGHT);
}

void DrawSnake::updateSnakeAndFood(GridLocation point, deque<GridLocation> deque)
{
    food = point;
    deq = deque;
}

